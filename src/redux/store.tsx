import { createStore } from "redux";

const initialState = {
  loading: false,
};

const reducer = (state = initialState, action: any) => {
  if (action.type === "SET_LOADING") {
    return {
      ...state,
      loading: action.value,
    };
  }
  return state;
};

const store = createStore(reducer);

export default store;
